public class DataTypes {
    public static void main(String[] args) {
        byte n1 = 34;
        
        short n2 = 5000;
        
        int n3 = 10000;
        
        long n4 = 1500000000L;
        
        float n5 = 5.455f;
        
        double n6 = 12.343235321d;
        
        System.out.println(n1 + n2 + n3 + n4 + n5 + n6);
    }
}
